package d20191213_Basic;

public class VarEx03 {		// 클래스 시작
	public static void main(String[] args) {		// 메인 시작
		
		float f1 = 3.1415f;			// 실수형이므로 자료형 float으로 선언하고 float는 예외로 수 뒤에 f를 붙임
		double d1 = 3.1415;			// 실수형이므로 자료형 double 선언
		
		System.out.println("f1 : "+ f1);
		
		System.out.println("d1 :"+ d1);
		
		
		
		
	}	// 메인 끝
	
}	// 클래스 끝
