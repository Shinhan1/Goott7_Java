package d20191213_Basic;

// 비교 연산자
public class OperEx04 { 		// 클래스 시작
	
	public static void main(String[] args) {			// 메인 시작
		
		int a = 20;		// a에 20 대입
		int b = 10;		// b에 10 대입
		
		System.out.println(" a > b : "+ (a > b));		// 비교 연산 시작
		System.out.println(" a >= b : "+ (a >= b));
		System.out.println(" a < b : "+ (a < b));
		System.out.println(" a <= b : "+ (a <= b));
		System.out.println(" a == b : "+ (a == b));
		System.out.println(" a != b : "+ (a != b));
		
	}	// 메인 끝

}	// 클래스 끝
