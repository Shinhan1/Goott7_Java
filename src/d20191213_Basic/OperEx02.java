package d20191213_Basic;

// 산술 연산자 : +, -, *, /, %

public class OperEx02 {			// 클래스 시작
	
	public static void main(String[] args) { 			// 메인 시작
		int a = 100;			// a에 100 대입
		int b = 200;			// b에 200 대입
			
		System.out.println(" a + b = "+ a + b);			// 사칙연산
		System.out.println(" a + b = "+ (a + b));		
		System.out.println(" a - b = "+ (a - b));		
		System.out.println(" a * b = "+ (a * b));		
		System.out.println(" a / b = "+ (a / b));		
		System.out.println(" a % b = "+ (a % b));		
		
		
		
	}	// 메인 끝

}	// 클래스 끝
